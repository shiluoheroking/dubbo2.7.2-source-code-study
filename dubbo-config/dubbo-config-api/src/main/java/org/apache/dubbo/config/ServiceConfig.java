    public void checkAndUpdateSubConfigs() {
        // Use default configs defined explicitly on global configs
        completeCompoundConfigs();
        // Config Center should always being started first.
        startConfigCenter();
        checkDefault();
        checkProtocol();
        checkApplication();
        // if protocol is not injvm checkRegistry
        if (!isOnlyInJvm()) {
            checkRegistry();
        }
        this.refresh();
        checkMetadataReport();

        if (StringUtils.isEmpty(interfaceName)) {
            throw new IllegalStateException("<dubbo:service interface=\"\" /> interface not allow null!");
        }

        if (ref instanceof GenericService) {
            interfaceClass = GenericService.class;
            if (StringUtils.isEmpty(generic)) {
                generic = Boolean.TRUE.toString();
            }
        } else {
            try {
                interfaceClass = Class.forName(interfaceName, true, Thread.currentThread()
                        .getContextClassLoader());
            } catch (ClassNotFoundException e) {
                throw new IllegalStateException(e.getMessage(), e);
            }
            checkInterfaceAndMethods(interfaceClass, methods);
            checkRef();
            generic = Boolean.FALSE.toString();
        }
        if (local != null) {
            if ("true".equals(local)) {
                local = interfaceName + "Local";
            }
            Class<?> localClass;
            try {
                localClass = ClassUtils.forNameWithThreadContextClassLoader(local);
            } catch (ClassNotFoundException e) {
                throw new IllegalStateException(e.getMessage(), e);
            }
            if (!interfaceClass.isAssignableFrom(localClass)) {
                throw new IllegalStateException("The local implementation class " + localClass.getName() + " not implement interface " + interfaceName);
            }
        }
        if (stub != null) {
            if ("true".equals(stub)) {
                stub = interfaceName + "Stub";
            }
            Class<?> stubClass;
            try {
                stubClass = ClassUtils.forNameWithThreadContextClassLoader(stub);
            } catch (ClassNotFoundException e) {
                throw new IllegalStateException(e.getMessage(), e);
            }
            if (!interfaceClass.isAssignableFrom(stubClass)) {
                throw new IllegalStateException("The stub implementation class " + stubClass.getName() + " not implement interface " + interfaceName);
            }
        }
        checkStubAndLocal(interfaceClass);
        checkMock(interfaceClass);
    }

    /**
     * Determine if it is injvm
     *
     * @return
     */
    private boolean isOnlyInJvm() {
        return getProtocols().size() == 1 && LOCAL_PROTOCOL.equalsIgnoreCase(getProtocols().get(0).getName());
    }

    public synchronized void export() {
        // 服务暴露前，对各种参数进行检查
        checkAndUpdateSubConfigs();

        // 通过配置可以指定某些服务不需要被暴露，比如：在 UserServiceImpl 服务实现类上指定 `@Service(export = false)` 配置，则认为当前不符需要被暴露
        if (!shouldExport()) {
            return;
        }

        // 延迟暴露服务，比如：在 UserServiceImpl 服务实现类上指定 `@Service(delay = 1000)` 配置，则当前服务延迟1秒后在进行暴露
        if (shouldDelay()) {
            delayExportExecutor.schedule(this::doExport, getDelay(), TimeUnit.MILLISECONDS);
        } else {
            // 开始暴露服务
            doExport();
        }
    }

    private boolean shouldExport() {
        Boolean export = getExport();
        // default value is true
        return export == null ? true : export;
    }

    @Override
    public Boolean getExport() {
        return (export == null && provider != null) ? provider.getExport() : export;
    }

    private boolean shouldDelay() {
        Integer delay = getDelay();
        return delay != null && delay > 0;
    }

    @Override
    public Integer getDelay() {
        return (delay == null && provider != null) ? provider.getDelay() : delay;
    }

    protected synchronized void doExport() {
        if (unexported) {
            throw new IllegalStateException("The service " + interfaceClass.getName() + " has already unexported!");
        }
        if (exported) {
            return;
        }
        exported = true;

        // 1. path属性即要暴露的服务的接口全类名，比如：com.dubbo.service.UserService
        if (StringUtils.isEmpty(path)) {
            path = interfaceName;
        }
        // 2. 开始暴露服务
        doExportUrls();
    }

    private void checkRef() {
        // reference should not be null, and is the implementation of the given interface
        if (ref == null) {
            throw new IllegalStateException("ref not allow null!");
        }
        if (!interfaceClass.isInstance(ref)) {
            throw new IllegalStateException("The class "
                    + ref.getClass().getName() + " unimplemented interface "
                    + interfaceClass + "!");
        }
    }

    public synchronized void unexport() {
        if (!exported) {
            return;
        }
        if (unexported) {
            return;
        }
        if (!exporters.isEmpty()) {
            for (Exporter<?> exporter : exporters) {
                try {
                    exporter.unexport();
                } catch (Throwable t) {
                    logger.warn("Unexpected error occured when unexport " + exporter, t);
                }
            }
            exporters.clear();
        }
        unexported = true;
    }

    @SuppressWarnings({"unchecked", "rawtypes"})
    private void doExportUrls() {
        /**
         * 1. 获取注册中心URL，为什么是List<URL>呢？因为Dubbo支持多注册中心，以Springboot项目为例，在xml文件中可以使用如下方式配置多注册中心，
         *      myregistry1注册中心使用了zookeeper，myregistry2注册中心使用了redis：
         *      dubbo.registrys.myregistry1.address=zookeeper://127.0.0.1:2181,dubbo.registrys.myregistry2.address=redis://127.0.0.1:6379
         *
         * 具体支持的注册中心可以参考dubbo官网：http://dubbo.apache.org/zh/docs/v2.7/user/references/registry/
         */
        List<URL> registryURLs = loadRegistries(true);
        for (ProtocolConfig protocolConfig : protocols) {
            String pathKey = URL.buildKey(getContextPath(protocolConfig).map(p -> p + "/" + path).orElse(path), group, version);
            ProviderModel providerModel = new ProviderModel(pathKey, ref, interfaceClass);
            ApplicationModel.initProviderModel(pathKey, providerModel);

            /**
             * 2. 遍历多协议配置，开始在每个注册中心开始注册服务，同时进行服务暴露
             *
             *    dubbo也支持多协议的配置，和多注册中心类似，即同一个服务可以以多种协议进行发布。
             *    比如 UserService 服务同时可以发布 dubbo协议、rest协议、http协议、webservice协议等等的服务，即我们可以通过配置的协议进行服务调用
             *    具体可以参考dubbo官网：http://dubbo.apache.org/zh/docs/v2.7/user/references/protocol/
             */
            doExportUrlsFor1Protocol(protocolConfig, registryURLs);
        }
    }

    private void doExportUrlsFor1Protocol(ProtocolConfig protocolConfig, List<URL> registryURLs) {
        String name = protocolConfig.getName();
        if (StringUtils.isEmpty(name)) {
            name = DUBBO;
        }

        Map<String, String> map = new HashMap<String, String>();
        map.put(SIDE_KEY, PROVIDER_SIDE);

        appendRuntimeParameters(map);
        appendParameters(map, metrics);
        appendParameters(map, application);
        appendParameters(map, module);
        // remove 'default.' prefix for configs from ProviderConfig
        // appendParameters(map, provider, Constants.DEFAULT_KEY);
        appendParameters(map, provider);
        appendParameters(map, protocolConfig);
        appendParameters(map, this);
        if (CollectionUtils.isNotEmpty(methods)) {
            for (MethodConfig method : methods) {
                appendParameters(map, method, method.getName());
                String retryKey = method.getName() + ".retry";
                if (map.containsKey(retryKey)) {
                    String retryValue = map.remove(retryKey);
                    if ("false".equals(retryValue)) {
                        map.put(method.getName() + ".retries", "0");
                    }
                }
                List<ArgumentConfig> arguments = method.getArguments();
                if (CollectionUtils.isNotEmpty(arguments)) {
                    for (ArgumentConfig argument : arguments) {
                        // convert argument type
                        if (argument.getType() != null && argument.getType().length() > 0) {
                            Method[] methods = interfaceClass.getMethods();
                            // visit all methods
                            if (methods != null && methods.length > 0) {
                                for (int i = 0; i < methods.length; i++) {
                                    String methodName = methods[i].getName();
                                    // target the method, and get its signature
                                    if (methodName.equals(method.getName())) {
                                        Class<?>[] argtypes = methods[i].getParameterTypes();
                                        // one callback in the method
                                        if (argument.getIndex() != -1) {
                                            if (argtypes[argument.getIndex()].getName().equals(argument.getType())) {
                                                appendParameters(map, argument, method.getName() + "." + argument.getIndex());
                                            } else {
                                                throw new IllegalArgumentException("Argument config error : the index attribute and type attribute not match :index :" + argument.getIndex() + ", type:" + argument.getType());
                                            }
                                        } else {
                                            // multiple callbacks in the method
                                            for (int j = 0; j < argtypes.length; j++) {
                                                Class<?> argclazz = argtypes[j];
                                                if (argclazz.getName().equals(argument.getType())) {
                                                    appendParameters(map, argument, method.getName() + "." + j);
                                                    if (argument.getIndex() != -1 && argument.getIndex() != j) {
                                                        throw new IllegalArgumentException("Argument config error : the index attribute and type attribute not match :index :" + argument.getIndex() + ", type:" + argument.getType());
                                                    }
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        } else if (argument.getIndex() != -1) {
                            appendParameters(map, argument, method.getName() + "." + argument.getIndex());
                        } else {
                            throw new IllegalArgumentException("Argument config must set index or type attribute.eg: <dubbo:argument index='0' .../> or <dubbo:argument type=xxx .../>");
                        }

                    }
                }
            } // end of methods for
        }

        if (ProtocolUtils.isGeneric(generic)) {
            map.put(GENERIC_KEY, generic);
            map.put(METHODS_KEY, ANY_VALUE);
        } else {
            String revision = Version.getVersion(interfaceClass, version);
            if (revision != null && revision.length() > 0) {
                map.put(REVISION_KEY, revision);
            }

            String[] methods = Wrapper.getWrapper(interfaceClass).getMethodNames();
            if (methods.length == 0) {
                logger.warn("No method found in service interface " + interfaceClass.getName());
                map.put(METHODS_KEY, ANY_VALUE);
            } else {
                map.put(METHODS_KEY, StringUtils.join(new HashSet<String>(Arrays.asList(methods)), ","));
            }
        }
        if (!ConfigUtils.isEmpty(token)) {
            if (ConfigUtils.isDefault(token)) {
                map.put(TOKEN_KEY, UUID.randomUUID().toString());
            } else {
                map.put(TOKEN_KEY, token);
            }
        }

        /********************** 以上所有操作就是把各种参数封装到map里面，供后面服务暴露使用 *******************************/

        // export service
        /**
         * 1. 获取注册中心的主机和端口号
         */
        String host = this.findConfigedHosts(protocolConfig, registryURLs, map);
        Integer port = this.findConfigedPorts(protocolConfig, name, map);
        URL url = new URL(name, host, port, getContextPath(protocolConfig).map(p -> p + "/" + path).orElse(path), map);

        /**
         * 2. 通过控制台修改相关参数，就是在这个地方生效的
         */
        if (ExtensionLoader.getExtensionLoader(ConfiguratorFactory.class)
                .hasExtension(url.getProtocol())) {
            url = ExtensionLoader.getExtensionLoader(ConfiguratorFactory.class)
                    .getExtension(url.getProtocol()).getConfigurator(url).configure(url);
        }

        /**
         * 3. 获取服务暴露的范围。意思就是说当前服务要暴露在那些地方，比如injvm（本地）、remote（远程）
         *
         *    注意：如果配置了在远程发布服务，同时也会在本地发布服务
         */
        String scope = url.getParameter(SCOPE_KEY);
        // don't export when none is configured
        if (!SCOPE_NONE.equalsIgnoreCase(scope)) {

            // export to local if the config is not remote (export to remote only when config is remote)
            /**
             * 3.1: 在本地发布服务
             */
            if (!SCOPE_REMOTE.equalsIgnoreCase(scope)) {
                exportLocal(url);
            }
            // export to remote if the config is not local (export to local only when config is local)
            /**
             * 3.2: 在远程发布服务
             */
            if (!SCOPE_LOCAL.equalsIgnoreCase(scope)) {
                if (!isOnlyInJvm() && logger.isInfoEnabled()) {
                    logger.info("Export dubbo service " + interfaceClass.getName() + " to url " + url);
                }
                if (CollectionUtils.isNotEmpty(registryURLs)) {
                    for (URL registryURL : registryURLs) {
                        //if protocol is only injvm ,not register
                        /**
                         * registryURL: registry://127.0.0.1:2181/org.apache.dubbo.registry.RegistryService?application=dubbo-provider&dubbo=2.0.2&pid=79155&qos-enable=false&registry=zookeeper&release=2.7.2&timestamp=1605855510231
                         */
                        if (LOCAL_PROTOCOL.equalsIgnoreCase(url.getProtocol())) {
                            continue;
                        }
                        url = url.addParameterIfAbsent(DYNAMIC_KEY, registryURL.getParameter(DYNAMIC_KEY));
                        URL monitorUrl = loadMonitor(registryURL);
                        if (monitorUrl != null) {
                            url = url.addParameterAndEncoded(MONITOR_KEY, monitorUrl.toFullString());
                        }
                        if (logger.isInfoEnabled()) {
                            logger.info("Register dubbo service " + interfaceClass.getName() + " url " + url + " to registry " + registryURL);
                        }

                        // For providers, this is used to enable custom proxy to generate invoker
                        String proxy = url.getParameter(PROXY_KEY);
                        if (StringUtils.isNotEmpty(proxy)) {
                            registryURL = registryURL.addParameter(PROXY_KEY, proxy);
                        }

                        /**
                         * 3.3: 通过动态代理的方式对服务接口生成invoker对象，其中ref是服务的具体实现类，interfaceClass是服务的接口
                         *
                         *      `ProxyFactory proxyFactory = ExtensionLoader.getExtensionLoader(ProxyFactory.class).getAdaptiveExtension();`
                         *
                         *      proxyFactory是一个通过SPI（Service Provider Interface）机制实现的灵活扩展，通过SPI机制可以通过配置来实现代理对象的生成方式。
                         *      dubbo源码中通过大量使用SPI机制来实现框架的灵活扩展，源码中长采用的SPI有三种实现，分别是静态扩展点、自适应扩展点、激活扩展点。
                         *      自适应扩展点分为两种，一种是被 `@Adaptive注解` 标识在接口上，另外一种是被 `@Adaptive注解` 标识在方法上。
                         *
                         *      这里采用的是标识在方法上的自适应扩展点，默认采用的是 `@SPI(javassist)注解` 中指定的动态代理方式，也可通过配置其他的动态代理，
                         *      可以在 META-INF/dubbo/internal/org.apache.dubbo.rpc.ProxyFactory 文件中看到，有三种动态代理的实现方式，分别是 stub、jdk、javassist。
                         *      此处的proxyFactory使用的javassist，所以 getInvoker方法会调用JavassistProxyFactory.getInvoker() 方法生成动态代理invoker对象。
                         *
                         *      下面就是一个通过SPI生成的一个 ProxyFactory$Adaptive，通过调用 ProxyFactory$Adaptive.getProxy()方法，若默认使用的是javassist动态代理实现，
                         *      在通过 org.apache.dubbo.rpc.ProxyFactory extension =
                         *                 (org.apache.dubbo.rpc.ProxyFactory) ExtensionLoader.getExtensionLoader(org.apache.dubbo.rpc.ProxyFactory.class).getExtension(extName);
                         *      就获取到了 org.apache.dubbo.rpc.proxy.javassist.JavassistProxyFactory 对象，从而使得最后调用了 JavassistProxyFactory.getProxy() 方法。
                         *
                         *      ```java
                         *          public class ProxyFactory$Adaptive implements org.apache.dubbo.rpc.ProxyFactory {
                         *     public java.lang.Object getProxy(org.apache.dubbo.rpc.Invoker arg0) throws org.apache.dubbo.rpc.RpcException {
                         *         if (arg0 == null) throw new IllegalArgumentException("org.apache.dubbo.rpc.Invoker argument == null");
                         *         if (arg0.getUrl() == null) throw new IllegalArgumentException("org.apache.dubbo.rpc.Invoker argument getUrl() == null");
                         *         org.apache.dubbo.common.URL url = arg0.getUrl();
                         *         String extName = url.getParameter("proxy", "javassist");
                         *         if (extName == null) throw new IllegalStateException(
                         *                 "Failed to get extension (org.apache.dubbo.rpc.ProxyFactory) name from url (" + url.toString() + ") use keys([proxy])");
                         *         org.apache.dubbo.rpc.ProxyFactory extension =
                         *                 (org.apache.dubbo.rpc.ProxyFactory) ExtensionLoader.getExtensionLoader(org.apache.dubbo.rpc.ProxyFactory.class).getExtension(extName);
                         *         return extension.getProxy(arg0);
                         *     }
                         *
                         *     public java.lang.Object getProxy(org.apache.dubbo.rpc.Invoker arg0, boolean arg1) throws org.apache.dubbo.rpc.RpcException {
                         *         if (arg0 == null) throw new IllegalArgumentException("org.apache.dubbo.rpc.Invoker argument == null");
                         *         if (arg0.getUrl() == null) throw new IllegalArgumentException("org.apache.dubbo.rpc.Invoker argument getUrl() == null");
                         *         org.apache.dubbo.common.URL url = arg0.getUrl();
                         *         String extName = url.getParameter("proxy", "javassist");
                         *         if (extName == null) throw new IllegalStateException(
                         *                 "Failed to get extension (org.apache.dubbo.rpc.ProxyFactory) name from url (" + url.toString() + ") use keys([proxy])");
                         *         org.apache.dubbo.rpc.ProxyFactory extension =
                         *                 (org.apache.dubbo.rpc.ProxyFactory) ExtensionLoader.getExtensionLoader(org.apache.dubbo.rpc.ProxyFactory.class).getExtension(extName);
                         *         return extension.getProxy(arg0, arg1);
                         *     }
                         *
                         *     public org.apache.dubbo.rpc.Invoker getInvoker(java.lang.Object arg0, java.lang.Class arg1, org.apache.dubbo.common.URL arg2)
                         *             throws org.apache.dubbo.rpc.RpcException {
                         *         if (arg2 == null) throw new IllegalArgumentException("url == null");
                         *         org.apache.dubbo.common.URL url = arg2;
                         *         String extName = url.getParameter("proxy", "javassist");
                         *         if (extName == null) throw new IllegalStateException(
                         *                 "Failed to get extension (org.apache.dubbo.rpc.ProxyFactory) name from url (" + url.toString() + ") use keys([proxy])");
                         *         org.apache.dubbo.rpc.ProxyFactory extension =
                         *                 (org.apache.dubbo.rpc.ProxyFactory) ExtensionLoader.getExtensionLoader(org.apache.dubbo.rpc.ProxyFactory.class).getExtension(extName);
                         *         return extension.getInvoker(arg0, arg1, arg2);
                         *     }
                         * }
                         *      ```
                         */
                        Invoker<?> invoker = proxyFactory.getInvoker(ref, (Class) interfaceClass, registryURL.addParameterAndEncoded(EXPORT_KEY, url.toFullString()));

                        /**
                         * 对Invoker对象进行一层包装，目的只有一个就是为了增强Invoker对象的能力。
                         * 下面protocol对象调用export方法在服务发布之前，中间还发生了一些事情，比如进行 qos（服务监控）、filter链（发布的服务过滤操作）、listener（服务监听）等等。
                         */
                        DelegateProviderMetaDataInvoker wrapperInvoker = new DelegateProviderMetaDataInvoker(invoker, this);

                        /**
                         * 3.4: 通过protocol.export() 进行服务发布。
                         *
                         *      此处的protocol同样使用了SPI技术，通过自适应扩展点调用目标的协议，
                         *
                         *      通过 `Protocol protocol = ExtensionLoader.getExtensionLoader(Protocol.class).getAdaptiveExtension();` 可以看出，在发布服务之前，
                         *      使用SPI技术，经过了qos（服务监控）、filter链（发布的服务过滤操作）、listener（服务监听）等ProtocolXXXWrapper类的export方法后，Protocol接口使用了
                         *      `@SPI("dubbo")` 标识，所以默认使用了 DubboProtocol协议进行服务发布，若配置了其他协议，则也可以使用其他协议进行服务的发布。
                         *      可以通过 META-INF/dubbo/internal/org.apache.dubbo.rpc.Protocol 文件看出，dubbo也提供了其他协议进行服务发布，比如：injvm、rmi、hession、http 等等 。
                         *
                         *      dubbo底层其实通过大量使用 <p>SPI机制</p> 来实现多协议的灵活扩展，牛逼牛逼。
                         *
                         *      关于qps、filter、listener里面的 export() 方法调用，感兴趣的可以自行debug下，这里重点分析服务发布的主流程，那么是由那个Protocol调用的DubboProtocol.export()的呢?
                         *      其实这里可以猜想一下，我们之前说过，在服务暴露的流程中会触发两件事情，其一：服务需要发布到注册中心上去；其二：启动netty并监听20880端口，
                         *
                         *      要想知道以上两步的触发时机，那么就需要先知道当前的Protocol对象到底指代的是谁，通过当前方法入口参数可以知道，此时Protocol应该指代的是RegistryProtocol，通过debug也可以验证这一点，
                         *      我在xml文件中配置了注册中心是本机的zookeeper，对应的registryURL如下:
                         *      registry://127.0.0.1:2181/org.apache.dubbo.registry.RegistryService?application=dubbo-provider&dubbo=2.0.2&pid=79155&qos-enable=false&registry=zookeeper&release=2.7.2&timestamp=1605855510231
                         *
                         *      所以接下来protocol.export()在启动netty服务之前一定会先调用RegistryProtocol.export() 方法，所以我们先进去 RegistryProtocol.export() 方法看看做了哪些事情。
                         *
                         */
                        Exporter<?> exporter = protocol.export(wrapperInvoker);
                        exporters.add(exporter);
                    }
                } else {

                    /**
                     * 这里服务发布流程和if里面的逻辑类似，只需要看if里面的逻辑即可。
                     */
                    Invoker<?> invoker = proxyFactory.getInvoker(ref, (Class) interfaceClass, url);
                    DelegateProviderMetaDataInvoker wrapperInvoker = new DelegateProviderMetaDataInvoker(invoker, this);

                    Exporter<?> exporter = protocol.export(wrapperInvoker);
                    exporters.add(exporter);
                }
                /**
                 * @since 2.7.0
                 * ServiceData Store
                 */
                MetadataReportService metadataReportService = null;
                if ((metadataReportService = getMetadataReportService()) != null) {
                    metadataReportService.publishProvider(url);
                }
            }
        }
        this.urls.add(url);
    }

